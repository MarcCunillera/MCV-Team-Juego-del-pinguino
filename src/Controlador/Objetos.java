package Controlador;

public class Objetos {
	
	// Atributos de los Objetos
	
	private int pez;
	private int nieve;
	
	// Constructor de los Atributos
	
	public Objetos(int pez, int nieve) {
	
		this.pez = pez;
		this.nieve = nieve;
		
	}
	
	// Getters y Setters de los atributos

	public int getPez() {
		return pez;
	}

	public void setPez(int pez) {
		this.pez = pez;
	}

	public int getNieve() {
		return nieve;
	}

	public void setNieve(int nieve) {
		this.nieve = nieve;
	}
	
	
	
	
}
